    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        String emailDigitado = campoEmail.getText();
        String senhaDigitada = campoSenha.getText();

        boolean loginValido = false;

        for (Cliente cliente : BancoUsuarios.listaUsuarios) {
            if (cliente.getEmail().equals(emailDigitado) && cliente.getSenha().equals(senhaDigitada)) {
                loginValido = true;

                // Salvar cliente na sess√£o
                SessaoUsuario sessao = SessaoUsuario.getInstancia();
                sessao.setUsuarioLogado(cliente);

                // Abrir tela de perfil com os dados do cliente
                ConsultaPerfil_back telaPerfil = new ConsultaPerfil_back(cliente);
                telaPerfil.setVisible(true);
                this.dispose(); // fecha a tela de login
                break;
            }
        }

        if (!loginValido) {
            JOptionPane.showMessageDialog(null, "E-mail ou senha incorretos!");
        }
    }//GEN-LAST:event_jButton8ActionPerformed

