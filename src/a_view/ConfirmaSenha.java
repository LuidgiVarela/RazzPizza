    private void jButton13ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton13ActionPerformed
    String novaSenha = campoSenha1.getText();
    String confirmarSenha = campoSenha2.getText();

    if (!novaSenha.equals(confirmarSenha)) {
        JOptionPane.showMessageDialog(null, "As senhas n√£o coincidem. Tente novamente.");
        return;
    }

    boolean senhaAlterada = false;

    for (Cliente c : BancoUsuarios.listaUsuarios) {
        if (c.getEmail().equalsIgnoreCase(emailUsuario)) {
            c.setSenha(novaSenha);
            senhaAlterada = true;
            break;
        }
    }

    if (senhaAlterada) {
        JOptionPane.showMessageDialog(null, "‚úÖ Senha atualizada com sucesso!");
        LoginCliente_back telaLogin = new LoginCliente_back();
        telaLogin.setVisible(true);
        this.dispose();
    } else {
        JOptionPane.showMessageDialog(null, "‚ùå Usu√°rio n√£o encontrado.");
    }
    }//GEN-LAST:event_jButton13ActionPerformed

